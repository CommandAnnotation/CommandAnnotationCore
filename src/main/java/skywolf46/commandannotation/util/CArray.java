package skywolf46.commandannotation.util;

public class CArray {
    public static String[] string(int amount) {
        return new String[amount];
    }

    public static Integer[] integer(int amount) {
        return new Integer[amount];
    }

    public static Float[] floats(int amount) {
        return new Float[amount];
    }

    public static Double[] doubles(int amount) {
        return new Double[amount];
    }

    public static Long[] longs(int amount) {
        return new Long[amount];
    }
}
